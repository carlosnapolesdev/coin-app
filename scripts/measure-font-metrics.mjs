#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join } from 'node:path'
import * as fontkit from 'fontkit'

const FAMILIES = [
  { name: 'Inter', file: 'Inter-Variable.woff2' },
  { name: 'Space Grotesk', file: 'SpaceGrotesk-Variable.woff2' },
]
const FALLBACK = ['ui-sans-serif', 'system-ui', '-apple-system', 'sans-serif']

const inDir = process.env.FONTS_IN_DIR
const outDir = process.env.FONTS_OUT_DIR ?? join(process.cwd(), 'src')
if (!inDir) {
  console.error('FONTS_IN_DIR env var is required')
  process.exit(1)
}
mkdirSync(outDir, { recursive: true })

const blocks = []
for (const f of FAMILIES) {
  const buf = readFileSync(join(inDir, f.file))
  const font = fontkit.create(buf)
  const upem = font.unitsPerEm
  const ascent = (font.ascent / upem) * 100
  const descent = (-font.descent / upem) * 100
  const lineGap = (font.lineGap / upem) * 100
  const sizeAdjust = 100
  const localSources = FALLBACK.map((name) => `local(${JSON.stringify(name)})`).join(', ')
  blocks.push(`@font-face {
  font-family: '${f.name} Fallback';
  src: ${localSources};
  size-adjust: ${sizeAdjust.toFixed(2)}%;
  ascent-override: ${ascent.toFixed(2)}%;
  descent-override: ${descent.toFixed(2)}%;
  line-gap-override: ${lineGap.toFixed(2)}%;
}`)
}

const output = `/*
 * Auto-generated by scripts/measure-font-metrics.mjs.
 * Do not edit by hand. Re-run the script to update after a font version bump.
 */
${blocks.join('\n\n')}
`

writeFileSync(join(outDir, 'fonts-metrics.css'), output)
process.stdout.write(`Wrote ${join(outDir, 'fonts-metrics.css')}\n`)
